package com;

public class Student {
	private String name;
	private String usn;
	private String dept; 
	public String getName(){
	      return name;
	}
	public String getUsn(){
	      return usn;
	}
	public String getDept(){
	      return dept;
	}
	public void setName(String name){
	      this.name = name;
	}
	public void setUsn(String usn){
	      this.usn = usn;
	}
	public void setDept(String dept){
	      this.dept = dept;
	}
}
